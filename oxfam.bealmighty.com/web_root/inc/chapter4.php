                                        <h3>Carpal Tunnel syndrome</h3>
                                        <p>Repetition, force, awkward and static postures lead to musculoskeletal disorders such as carpal tunnel syndrome. Poultry workers develop Carpal Tunnel Syndrome (CTS) at 7 times the average</p>
                                        <hr/>
                                        <div class="small-text">
                                            <p>“America's growing demand for specialty cuts presents an ergonomic nightmare for workers.”</p>
                                            <p><strong>Charlotte Observer, Feb 10, 2008</strong></p>
                                        </div>
                                        <p class="source">Sources: OSHA, Prevention of Musculoskeletal Injuries in Poultry Processing</p>
