                        <h3>Epidemic of repetitive strain injuries</h3>
                        <hr/>
                        <p>Standing in one spot, a line worker makes the same cutting, pulling, and hanging motions tens of thousands of time each day. Among the most common problems from repetitive strain:</p>
                        <ul>
                            <li>Pain in hands, swelling, numbness, loss of grip</li>
                            <li>carpal tunnel syndrome and tendonitis</li>
                            <li>“claw hand” (injured fingers lock in a curled position) </li>
                            <li>ganglionic cysts (fluid deposits under the skin)</li>
                            <li>trigger finger (a finger gets stuck in a bent position, and straightens with a snap--like a trigger being pulled and released).</li>
                        </ul>
                    </div>
