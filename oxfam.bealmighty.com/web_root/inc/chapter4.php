                                        <h3>Slips and Falls</h3>
                                        <p>Slips and falls are common, as the floors in poultry plants are usually coated with water, blood, or poultry fat and grease. Some people report working while standing in a pool of blood. </p>
                                        <hr/>
                                        <div class="small-text">
                                            <p>“Everything was wet—drenched in water, fat, blood and offal—dripping down gutters and gushing down drains. The smell of flesh, although not fetid, was raw and gamy.” </p>
                                            <p><strong>Jesse Katz, LA Times</strong></p>
                                        </div>
                                        <p class="source">Sources: Sources: Midwest Coalition for Human Rights, “Always Working Beyond the Capacity of Our Bodies: Meat and Poultry Processing Work Conditions and Human Rights in the Midwest,” University of Minnesota Human Rights Program in the Institute of Global Studies, October 2012. | Jesse Katz, LA Times, http://www.byjessekatz.com/chicken2.pdf</p>
                                    </div>
