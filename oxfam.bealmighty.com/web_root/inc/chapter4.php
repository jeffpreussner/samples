                        <h3>Vulnerable populations</h3>
                        <ul>
                            <li><strong>Immigrants:</strong> Many poultry workers today are from other countries, including Mexico, Guatemala, El Salvador, Peru, Ecuador, Somalia, Laos, the Marshall Islands, Nepal, China, and Haiti. These workers face many challenges in learning about and understanding their rights.<br><br>While plants often employ people both English and Spanish-speakers, they rarely provide translators for speakers of other languages. The language barrier makes it difficult for workers to communicate with each other and to speak out.<br><br>Many workers have tenuous immigration status, which makes them especially vulnerable. They fear deportation, which could put families in the US and in their country of origin at risk.</li>
                            <li><strong>Refugees:</strong> In recent years, the poultry industry has been turning to an especially vulnerable population: refugees who come to the US seeking asylum. In a plant in Texas, Pilgrim’s employs refugees from Burma, while other companies employ refugees from Sudan, Iraq, Eritrea, and Somalia. Refugees are apt to be paid even less than other workers because the poultry industry often uses labor contractors to find these refugees; contractors pay wages directly to the workers--usually less than the plant would pay.</li>
                            <li><strong>Prisoners:</strong> In some plants, prisoners serve as another source of labor.</li>
