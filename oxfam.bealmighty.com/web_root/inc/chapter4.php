                        <p>Imagine going to work every day knowing you may not be allowed to go to the bathroom for many hours during your shift. You debate wearing an adult diaper because of that. You are likely among the 86% of workers who suffer hand and wrist pain because of repeating the same motions 20,000 times each shift. You face another day of the same. Should your pain become chronic, you risk permanent damage to your hands and arms. Company nurses dismiss your concerns, sending you back to work. Still, the pressure to keep up with the speed of the line is unrelenting. And the check at the end of the week barely sustains you and your family.</p>
                        <p>The poultry industry puts workers’ lives on the line every day.</p>
                        <h3>Surrounded by perils</h4>
                            <hr/>
                    </div>
                </div>
            </div>
            <div class="row">
                <div class="col-md-5 col-md-offset-1">
                    <div class="text_overlay m-top-0 mobile-m-bottom-0 mobile-m-top-0">
                        <p>Ask a poultry worker about what happens on the line, and they usually start pointing to places on their body: scars from surgery; marks from cuts, claws, or bites; swollen hands and wrists.</p>
                        <p>Working on the line is working through pain and problems. The US Department of Labor officially classifies poultry as a “hazardous industry” and has calculated that poultry workers are injured five times more than other workers.</p>
                        <p>The industry compounds the danger: increasing line speeds, failing to provide rest breaks, providing inadequate medical care to injured workers, underreporting incidents of injury and illness, and denying responsibility for workers who become injured or disabled.</p>
