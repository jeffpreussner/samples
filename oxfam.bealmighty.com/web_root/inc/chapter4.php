                                        <h3>Repetitive Motion</h3>
                                        <p>Performing the same forceful cutting, hanging, pulling, or trimming motion more than 20,000 times a day can lead to musculoskeletal disorders in poultry workers’ hands, arms, and shoulders. Most surveys reveal that a vast majority of workers experience pain, swelling in the joints, limited range of motion, numbness or tingling sensations, and loss of strength.</p>
                                        <hr/>
                                        <div class="small-text">
                                            <p>"The pain I felt, the back pain, the pain in my hand was a sharp pain and hurt with any movements; when I would kneel, when I would bend over, when I twisted from side to side. In reference to my hand, it hurt as soon as I started hanging and moved my hand … after I was diagnosed with carpal tunnel my body started to get stiff. My arms became stiff, my feet very stiff, my legs, and my neck very stiff."</p>
                                            <p><strong>Roberto, former poultry plant worker</strong></p>
                                        </div>
                                        <p class="source">Sources: Kerry Hall, Ames Alexander, and Franco Ordoñez, “The Cruelest Cuts,” The Charlotte Observer, September 30, 2008. | Tom Fritzsche, Unsafe at These Speeds: Alabama’s Poultry Industry and Its Disposable Workers (2013).</p>
