                        <h3>Keeping medical treatment to “first aid”</h3>
                        <hr/>
                    </div>
                </div>
            </div>
            <div class="row">
                <div class="col-md-5 col-md-offset-1">
                    <div class="text_overlay m-top-0 mobile-m-top-0 mobile-m-bottom-0">
                        <p>When workers do have the courage to seek medical care, they are usually referred to medical personnel who work for the company. These people can be more interested in protecting the company than the health of the workers.</p>
                        <p>Plants are concerned about their safety records. They try to minimize medical treatments that would require recording or reporting incidents.</p>
                        <p>This means keeping medical attention at the level of first aid: ibuprofen for pain, compresses for swelling, cream for sore muscles. If companies can avoid doing more than this, they don’t have to record the incident, or report to the US government’s Occupational Health and Safety Administration (OSHA). This keeps their record clean, but also means they mistreat or misdiagnose serious injuries and illnesses.</p>
                        <p>When the condition requires attention from a physician, workers report that they are usually referred to doctors who work closely with the company. These doctors may avoid diagnoses that would make the company responsible.</p>
                        <p>If a worker requests the opportunity to see another doctor or a specialist, the company may discourage or threaten that worker.</p>
