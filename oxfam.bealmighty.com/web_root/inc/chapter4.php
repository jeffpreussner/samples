                                        <h3>Respiratory problems</h3>
                                        <p>Ammonia leaks are not uncommon in poultry plants, and often send workers to the hospital. Exposure to ammonia can result in severe burns to the eyes, skin, throat or lungs. In extreme cases, these burns can be serious enough to cause blindness, lung disease, and even death.</p>
                                        <hr/>
                                        <div class="small-text">
                                            <p>"On August 31st 2013 … I was working in packing; there was an ammonia leak. Since then I have breathing problems … my chest tightens suddenly and … it is hard to breathe." </p>
                                            <p><strong>Rosa, former Tyson poultry plant worker</strong></p>
                                        </div>
                                        <p class="source">Sources: Public Health Statement for Ammonia, Agency for Toxic Substances & Disease Registry, September 2004</p>
