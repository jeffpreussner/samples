                                        <h3>Antibiotic resistance</h3>
                                        <p>In the process of handling the flesh of thousands of birds each day, poultry workers absorb enough antibiotics that they develop resistance to them.</p>
                                        <p>In one study, poultry workers had 32 times greater odds of being colonized with antibiotic-resistant E. coli than other members of the community. Health experts have reported instances of these workers developing staph infections that are potentially fatal, as they are resistant to treatment.</p>
                                        <p class="source">Sources: Johns Hopkins Bloomberg School of Public Healthy, Dec, 17, 2007.</p>
