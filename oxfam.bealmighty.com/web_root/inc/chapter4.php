                        <h3>Inadequate training</h3>
                        <hr/>
                        <p>Many companies provide scant training for workers on health, safety, or proper technique. What instruction workers receive is sometimes offered in a language that cannot be understood by all trainees. They are then put to work almost immediately. Consequently—in the absence of proper training—new hires must often assume their position on the line and, while rushing to keep up, watch what other more experienced workers are doing. Naturally, these new workers frequently end up holding or using their tools incorrectly, which can lead to injuries.</p>
