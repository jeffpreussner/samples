                        <h3>Other medical risks</h3>
                        <hr/>
                        <ul>
                            <li>Cuts and lacerations: Many workers tell stories of losing the tips of fingers or whole fingers, or of stabbing themselves in the hand, arm, or leg.</li>
                            <li>Slips, trips, falls: Workers, who often wear heavy rubber boots as part of their protective gear, find it difficult to step on and off work platforms in wet and humid conditions.</li>
                            <li>Respiratory hazards: Because of chemicals, dust, and animal waste, poultry workers suffer an array of respiratory problems, from asthma to nausea.</li>
                            <li>Exposure to dangerous chemicals: Poultry workers are exposed to an array of toxins on the line.</li>
                            <li>Depression: Workers commonly develop depression and anxiety. Among the factors that cause this are social isolation and low social support, abusive supervision, poor compensation and living conditions, hazardous conditions, and job insecurity.<br><br>One study found that poultry workers from Perdue and other companies in North Carolina suffered depressive symptoms at a rate 80 percent higher than non-poultry workers.</li>
                        </ul>
