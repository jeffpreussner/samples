                        <h3>Cheating workers bit by bit</h3>
                        <hr/>
                        <p>Many plants chip away at workers’ pay.</p>
                        <ul>
                            <li><strong>Wage theft:</strong> Wage theft is common in the poultry industry. For example, workers are rarely paid for the time they spend preparing for and finishing up after work. In one survey, the Department of Labor found that 100 percent of poultry companies were out of compliance with compensation requirements for “donning and doffing” (putting on and taking off pieces of safety gear) and lunch periods.<br><br>In 2010, Pilgrim’s paid over $1 million in back wages to workers at a Dallas plant for overtime and “donning and doffing.” In 2008, Sanderson paid over $2.6 million in back wages to workers they had not paid properly for time they had worked.<br><br>In most plants, workers stay on the line until all the chickens are processed. While many work more than 40 hours a week, reports of overtime pay are rare.<br><br></li>
                            <li><strong>Questionable accounting practices:</strong> Some companies are now paying workers with debit cards. The cards may force workers to pay a fee (which is illegal), and require workers to go online to find out how much is on the card. Other plants decline to provide workers with paystubs. These practices make it difficult for workers to gather the evidence they would need to file a wage violation complaint.</li>
                            <li><strong>Paying for protective equipment:</strong> Workers are sometimes required to buy their own safety equipment; Perdue workers at plants in Maryland and Delaware report having to pay for their own boots, gloves, aprons, and goggles. In one study, 96.9 percent of the workers were required to either purchase or pay for replacements.</li>
                        </ul>
