                        <h3>Harassment and discrimination</h3>
                        <hr/>
                        <p>Poultry companies deliberately take advantage of the demographics of the workforce to create a climate of fear in processing plants.</p>
                        <p>Workers generally have more interaction with their line supervisor than any other personnel in the plant. This person has intimate power over workers on the line; supervisors manage line speed, control bathroom breaks, and access to medical personnel. </p>
                        <p>Some workers say their supervisors are fair, but many workers report that the supervisors—under intense pressure to keep up with production—push line workers to excess. There are frequent reports of rough, or even abusive, tactics. Many workers report that their requests for bathroom breaks are denied. Still others report that they are chastised or derided when they bring injuries or illnesses to their supervisor’s attention.</p>
                        <ul>
                            <li><strong>Point system:</strong> In the effort to control the workforce, many poultry plants use a “point system” to track tardiness, absences, mistakes—even injuries. When the company wants to penalize or dismiss a worker, it can refer to the points, with no other explanation. Supervisors rarely explain or document the system; many workers don’t know how many points they have or how close they are to being fired. So workers live in fear of points, of doing the wrong thing and being fired without warning.</li>
                            <li><strong>Bathroom breaks:</strong> Many workers are afraid to ask for permission to go to the bathroom. The supervisor, who needs to find a replacement worker to fill the spot, may yell, penalize, or threaten firing. <br><br>The long waits are especially hard for some workers, including those who are older or pregnant. Some workers take the step of reducing their intake of fluids. Pedro, a former poultry worker for Tyson, simply stopped drinking water: “I figured if I don't drink that much, I won't need to use the bathroom a lot.” He became sufficiently dehydrated that his potassium levels dropped, causing severe leg cramps. <br><br>Bacilio Castro, who worked at a Case Farms plant in North Carolina, recounts the story of a woman who was eight months’ pregnant being denied a bathroom break for two hours. When she eventually peed on herself, the supervisor screamed at her, and took her to the office for disciplinary action.</li>
                            <li><strong>Fear of losing jobs:</strong> Many poultry workers come to the US to support families back home. Their parents and relatives depend on them, and workers report deep pain when they can’t provide that support. Consequently workers are terrified of losing their jobs on the line.<br><br>Workers get the message: if they want to keep their jobs, they need to endure what happens inside the plant. Many report that the plant personnel tell them to deal with the conditions, or “There’s the door.”</li>
                        </ul>
