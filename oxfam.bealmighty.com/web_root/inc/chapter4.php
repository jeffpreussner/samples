                        <h3>POVERTY WAGES IN A BOOMING INDUSTRY</h3>
                        <!-- <hr/> -->
                        <p>While the poultry industry is profiting, workers are not. They earn low wages, have scant benefits, and have little, if any, job security. None of the workers interviewed by Oxfam reported receiving any paid time off.</p>
                        <p>Most poultry workers earn wages that leave them close to the poverty line. Wages average $10 to $11 per hour; annual full-time income is $20,000 to $25,000.  The average poultry worker supporting a family of four lives below the poverty line and qualifies for food stamps, Head Start, and the National School Lunch Program. And wages do not keep up with inflation; over the past 30 years, the real value of poultry workers’ wages has declined almost 40 percent.  </p>
                        <p>During this same period, profits have climbed steadily. Poultry executives are enjoying huge leaps in their pay</p>
                        <ul>
                            <li>The CEO and chairman of Sanderson Farms received $5.9 million in compensation in 2014, a nearly 200 percent increase since 2011.</li>
                            <li>The President and CEO of Tyson Foods earned $12.2 million in 2014: 550 times what the average poultry worker makes. Perhaps more stark, in three and a half hours, he earned the annual salary of a line worker.</li>
                            <li>The President and CEO of Pilgrim’s has seen his compensation rise 290 percent to $9.3 million since 2011.</li>
                        </ul>
                        <p>Most plants do not offer substantial raises, even to workers who’ve been there for years. Perdue workers in Maryland reported still earning barely $11 per hour, even after years on the job.</p>
