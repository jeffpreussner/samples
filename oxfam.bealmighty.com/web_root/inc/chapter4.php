                        <h3>Disabled workers with no recourse</h3>
                        <hr/>
                        <p>Cumulative injuries can build to the point that a worker can no longer use his hands, arms, or shoulders. And cannot work—at the poultry plant, or another job. Then what?</p>
                        <p>Workers' compensation  is designed to provide cash benefits and medical care to employees who suffer work-related injuries or illnesses. All states have a worker’s comp program. Work such as that done by poultry workers is normally covered but in practice, workers often face significant obstacles to accessing benefits. Plants often argue that the workers’ problems are not related to work, but to another activity. In one case, an employer blamed the repetitive strain injury on a car with manual transmission.</p>
                        <p>Unlike obvious injuries such as an amputation, repetitive strain can be difficult to prove. The damage is largely invisible (other than swelling), and only develops over time, rather than as a result of a single traumatic incident.</p>
