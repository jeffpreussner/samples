                        <p>Carpal tunnel syndrome is rampant in the poultry industry. Poultry workers suffer it at seven times the rate of other workers. It is not a case of short-term discomfort; as it presents in poultry workers, carpal tunnel syndrome can be a permanent and disabling condition. It often requires surgery and—if not properly treated—can prevent those affected from working. Without the normal use of their hands, these workers are unable to continue earning money to support themselves and their families.</p><p>Many workers say that when they ask to be transferred to a different position on the line, in order to let their muscles rest, supervisors often deny their requests.</p>
