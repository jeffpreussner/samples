<p>Roughly a quarter of a million people work on the processing line in American poultry plants. As they process the chicken we eat, they face dangerous conditions and poverty-level wages on a daily basis. These problems are industry-wide, but the four biggest poultry companies control nearly 60% of the chicken market. As industry leaders, they have the power to make changes to quickly improve conditions for their workers. But they won’t, unless they hear a loud and clear message from people like you. Please add your name below to demand respect for poultry workers’ rights.</p>
<h3>Tyson Foods, Pilgrim’s, Perdue, Sanderson Farms:</h3>
<p>I care where my chicken comes from – and I care deeply about the people who help get it to my family’s table. Workers in your factories deserve:</p>
<ul>
	<li>Fair pay and benefits </li>
	<li>A safe working environment, and</li>
	<li>A voice in the workplace.</li>
</ul>
<p>Please implement changes throughout your poultry plants and lead the way in ensuring that your workers have the right to safety, opportunity, and dignity in their labor. </p>
