						<p>These are tough jobs—and the industry doesn’t make it any easier for the workers. In fact, Big Poultry treats workers as replaceable cogs in their machine; turnover in poultry plants can reach as high as 100 percent every year. To find workers willing to do these jobs, the poultry industry exploits vulnerable people who have few other options: minorities, immigrants and refugees—even prisoners. Because of their precarious situations, most workers are afraid to speak out or do anything that might jeopardize their jobs.</p>
						<p>Poultry workers:</p>
						<ul>
							<li>suffer injuries at five times the rate of all workers, </li>
							<li>endure a climate of fear that makes it difficult for them to speak out about their work conditions, and</li>
							<li>earn poverty-level wages.</li>
						</ul>
						<p>Of roughly 250,000 US poultry workers, most are people of color, immigrants, or refugees, and many are women.
						<p>Most of us who eat chicken do so because we believe it is a healthy and more environmentally friendly source of protein than other meats. And many of us have spoken out to ensure that the conditions under which chickens are raised are improving. You are making lots of great choices—for your health and the environment—but Big Poultry is keeping some crucial information from you. </p>
						<p>You need to know what really goes on behind the walls of those poultry plants—and what you can you do to help.</p>
						<p>People like you have already pushed Big Poultry to change: Tyson Foods, Pilgrim’s, and Perdue have all recently pledged to phase out the use of antibiotics from their chicken supply chain. You spoke and they listened.  They’ll listen again.</p>
						<p>Fear has kept many poultry workers silent.  You don’t have to be. Speak up and help them share their stories.</p>
