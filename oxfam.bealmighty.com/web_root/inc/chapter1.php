						<p>The poultry industry is booming. Consumer demand is growing and profits are climbing. Executive compensation is soaring. </p>
						<p>Even the lives of chickens have begun to improve as consumers have voiced concern for their treatment. But one key part of this rocketing industry has been left behind: the people who work processing poultry. Every day, hundreds of thousands of workers become disposable parts of a large, fast, relentless machine. </p>
						<p>These women and men stand on the line hour after hour, faced with an endless stream of chicken carcasses. They hang, cut, debone, twist: repeating the same motions tens of thousands of times each shift, with few moments to rest, stretch, or take a bathroom break.</p>
						<p>The plants are cold, humid, and slippery with grease, offal, blood, and water. The air is full of chemicals from cleaning, processing, and cooking. The machines are loud and the tools are sharp. </p>
