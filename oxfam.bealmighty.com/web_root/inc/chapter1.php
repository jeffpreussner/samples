									<p><strong>And we eat a lot of it—89 pounds per person each year. Chicken is America’s most popular meat. From nuggets to sandwiches to wings, poultry is a $50-billion industry.</strong>
									<p>But how does all that chicken reach our plates? It’s not easy to turn a live bird into a bucket of buffalo wings. In fact, it takes a lot of work: hanging, cutting, trimming.</p>
									<p>And it takes many, many people working in plants under inhumane conditions: constant health and safety risks, low wages, and a lack of opportunity to speak out about these conditions. Consumers have called on the poultry industry to improve the treatment of chickens, but the plight of workers has received far too little attention. The workers themselves face obstacles that prevent them from speaking out freely about these conditions.</p>
									<p>We’ll show you what the poultry industry doesn’t want you to see, and tell you exactly what you can do to help workers in their struggle.</p>
