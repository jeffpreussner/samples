						<h3>Call on Big Poultry for Change</h3>
						<hr/>
						<p>Workers throughout the poultry industry face these problems. But the companies at the top have a responsibility to act. Four chicken companies control roughly 60 percent of the American poultry market: Tyson Foods, Pilgrim’s, Perdue, and Sanderson Farms. These companies are so big that if they make changes, other poultry companies will have to follow. They can and should implement changes that will improve conditions for poultry workers, by:</p>
						<ul>
							<li>providing a safe environment in plants, and caring for workers properly when they’re injured; and</li>
							<li>allowing workers to have a voice in the workplace, including the freedom to speak out about problems and greater input into the decisions that affect their work; and compensating workers fairly.</li>
						</ul>
