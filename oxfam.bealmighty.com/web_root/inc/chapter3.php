						<h3>A cruel machine that never stops running</h3>
						<hr/>
						<p>The world inside a poultry plant is not only harsh, but unhealthy. Conditions pose constant dangers to the women and men who work there.</p>
						<p>Imagine you are a line worker…</p>
						<p>You arrive for your shift dressed in bulky clothes. In most plants, the temperature hovers around 40 degrees F. This reduces microbial growth on the chicken carcasses—and it chills workers like you to the bone. The US government’s Occupational Health and Safety Administration (OSHA) notes that cold temperatures exacerbate the harmful effects of repetitive motions. You note that it causes your hands to stiffen and makes handling your tools harder.</p>
						<p>The plant is full of liquids. The birds produce blood, offal, and grease. Cleaning involves water, chlorine, detergent. Sometimes you spend hours on the line standing in a pool of blood.</p>
						<p>The line rarely stops or slows down. Your supervisor is under pressure to meet his daily production quota. This could mean that you will work a shift without any break.</p>
						<p>Each job on the line focuses on one small task, one single part of the bird: wing, leg, breast. So you repeats the same motion tens of thousands of time each shift. You wish you could rotate to different jobs on the line—to rest your muscles, learn new skills, and alleviate monotony. Workers tell you that the company often denies these requests.</p>
